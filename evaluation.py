# ---------------------------------------------------------------------------
# FILE PARSING
# ---------------------------------------------------------------------------
DELIMITER_PATTERN = re.compile(
    r"===\s*RESPONSE\s+(\d+)\s*===",
    re.IGNORECASE
)

def parse_txt_file(filepath):
    """
    Reads a .txt file and splits it into a list of response strings.
    Expected format:
        === RESPONSE 1 ===
        <text>
        === RESPONSE 2 ===
        <text>
        ...
    """
    with open(filepath, "r", encoding="utf-8") as f:
        content = f.read()
    return _split_by_delimiter(content)


def parse_docx_file(filepath):
    """
    Reads a .docx file, joins all paragraph text, then splits by delimiter.
    """
    try:
        from docx import Document
    except ImportError:
        raise ImportError("Install python-docx: pip install python-docx")

    doc = Document(filepath)
    full_text = "\n".join(para.text for para in doc.paragraphs)
    return _split_by_delimiter(full_text)


def _split_by_delimiter(content):
    """
    Splits content on === RESPONSE N === markers.
    Returns a dict: { response_number (int): response_text (str) }
    """
    parts = DELIMITER_PATTERN.split(content)
    # parts alternates: [pre-text, num, body, num, body, ...]
    responses = {}
    i = 1
    while i + 1 < len(parts):
        num   = int(parts[i])
        body  = parts[i + 1].strip()
        responses[num] = body
        i += 2
    return responses


def load_responses_from_file(filepath):
    """
    Auto-detects .txt vs .docx and returns a dict of {index: response_text}.
    """
    ext = os.path.splitext(filepath)[1].lower()
    if ext == ".txt":
        responses = parse_txt_file(filepath)
    elif ext in (".docx", ".doc"):
        responses = parse_docx_file(filepath)
    else:
        raise ValueError(f"Unsupported file type '{ext}'. Use .txt or .docx")

    print(f"  [INFO] Parsed {len(responses)} responses from '{filepath}'.")
    return responses

# ---------------------------------------------------------------------------
# EVALUATION HELPER
# ---------------------------------------------------------------------------
def evaluate_response(query, ai_response):
    """Uses Groq to judge the AI response."""
    prompt = (
        f"User Query: {query}\n\n"
        f"AI Response:\n{ai_response}\n\n"
        "Evaluate the response based on the system instructions and output JSON."
    )
    try:
        chat_completion = client.chat.completions.create(
            messages=[
                {"role": "system", "content": EVAL_SYSTEM_PROMPT},
                {"role": "user",   "content": prompt}
            ],
            model=EVAL_MODEL,
            response_format={"type": "json_object"},
            temperature=0.1,
        )
        result = chat_completion.choices[0].message.content
        return json.loads(result)
    except Exception as e:
        print(f"  [ERROR] Evaluation failed: {e}")
        return {
            "faithfulness":       0,
            "accuracy":           0,
            "hallucination_score": 0,
            "reasoning":          "Evaluation failed due to an error."
        }

# ---------------------------------------------------------------------------
# PROGRESS HELPERS
# ---------------------------------------------------------------------------
PROGRESS_FILE = "file_eval_progress.json"

def load_progress():
    if os.path.exists(PROGRESS_FILE):
        with open(PROGRESS_FILE, "r") as f:
            return json.load(f)
    return {"results": [], "next_index": 0}

def save_progress(progress):
    with open(PROGRESS_FILE, "w") as f:
        json.dump(progress, f, indent=4)

# ---------------------------------------------------------------------------
# MAIN
# ---------------------------------------------------------------------------
def run_file_evaluation(responses_filepath):
    # --- Load all responses from file ---
    print(f"\n[LOADING] Reading responses from: {responses_filepath}")
    responses = load_responses_from_file(responses_filepath)

    total = len(QUERIES)
    missing = [i + 1 for i in range(total) if (i + 1) not in responses]
    if missing:
        print(f"  [WARNING] Missing responses for query numbers: {missing}")
        print("  Those queries will be skipped.\n")

    # --- Resume support ---
    progress  = load_progress()
    results   = progress["results"]
    start_idx = progress["next_index"]

    if start_idx > 0:
        print(f"[RESUME] Resuming from query {start_idx + 1}/{total}. Press Enter to continue.")
        input()

    print("\n" + "=" * 60)
    print("  FILE-BASED EVALUATION — Indian Legal AI")
    print(f"  {total} queries | Responses loaded from file")
    print("=" * 60)

    for i in range(start_idx, total):
        query_num = i + 1
        query     = QUERIES[i]

        if query_num not in responses:
            print(f"\n  [{query_num}/{total}] SKIPPED — no response found in file.")
            progress["next_index"] = query_num
            save_progress(progress)
            continue

        ai_response = responses[query_num]

        print(f"\n{'─' * 60}")
        print(f"  EVALUATING [{query_num}/{total}]")
        print(f"  Q: {query[:80]}{'...' if len(query) > 80 else ''}")
        print(f"  Response length: {len(ai_response)} chars")

        # 2.5s delay to stay within Groq's 30 RPM free-tier rate limit
        if query_num > 1:
            time.sleep(2.5)

        eval_scores = evaluate_response(query, ai_response)

        f_score = eval_scores.get("faithfulness",       0)
        a_score = eval_scores.get("accuracy",           0)
        h_score = eval_scores.get("hallucination_score", 0)
        reason  = eval_scores.get("reasoning",          "")

        print(f"  Faithfulness   : {f_score}/100")
        print(f"  Accuracy       : {a_score}/100")
        print(f"  Hallucination  : {h_score}/100  (higher = less hallucination)")
        print(f"  Reasoning      : {reason}")

        results.append({
            "index":    query_num,
            "query":    query,
            "response": ai_response,
            "scores":   eval_scores
        })

        progress["results"]    = results
        progress["next_index"] = query_num
        save_progress(progress)
        print(f"  [Saved → {PROGRESS_FILE}]")

    # --- Final report ---
    evaluated = [
        r for r in results
        if not (r["scores"]["faithfulness"] == 0
                and r["scores"]["reasoning"] == "Evaluation failed due to an error.")
    ]
    count = len(evaluated)

    print("\n" + "=" * 60)
    print("  EVALUATION COMPLETE")
    print("=" * 60)

    if count == 0:
        print("No queries were successfully evaluated.")
        return

    avg_f = sum(r["scores"]["faithfulness"]        for r in evaluated) / count
    avg_a = sum(r["scores"]["accuracy"]            for r in evaluated) / count
    avg_h = sum(r["scores"]["hallucination_score"] for r in evaluated) / count

    print(f"  Total Evaluated         : {count}/{total}")
    print(f"  Avg Faithfulness        : {avg_f:.2f}/100")
    print(f"  Avg Accuracy            : {avg_a:.2f}/100")
    print(f"  Avg Hallucination Score : {avg_h:.2f}/100  (higher = better)")

    out_file = "evaluation_results.json"
    with open(out_file, "w") as f:
        json.dump({
            "summary": {
                "total_evaluated":         count,
                "avg_faithfulness":        round(avg_f, 2),
                "avg_accuracy":            round(avg_a, 2),
                "avg_hallucination_score": round(avg_h, 2)
            },
            "results": evaluated
        }, f, indent=4)

    print(f"\n  Full results saved → {out_file}")
    if os.path.exists(PROGRESS_FILE):
        print(f"  You may delete {PROGRESS_FILE} now.\n")


